def greet(name, location):
    # Write 3 print statements inside the function.
    print(f"Hello {name}")
    print(f"Good Morning! {name}")
    print(f"How ya doin, {name}?")
    print(f"Do you live in {location}?")
# Call the greet() function and run your code.

